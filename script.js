let currentLang = localStorage.getItem("wm-lab-lang") || "zh";
let currentOutputType = "papers";

function t(key) {
  return i18n[currentLang][key] || "";
}

function applyStaticText() {
  document.title = t("pageTitle");
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", t("pageDesc"));
  document.documentElement.lang = currentLang === "zh" ? "zh-CN" : "en";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  });
}

function renderMembers() {
  const wrap = document.getElementById("membersWrap");
  const groups = [
    { key: "researchers", title: t("memberGroupResearchers") },
    { key: "students", title: t("memberGroupStudents") },
    { key: "alumni", title: t("memberGroupAlumni") }
  ];

  wrap.innerHTML = groups
    .map((group) => {
      const cards = memberData[group.key]
        .map((m) => {
          const d = m[currentLang];
          return `<article class="member-card"><strong>${d.name}</strong><span>${d.role}</span><p>${d.focus}</p></article>`;
        })
        .join("");
      return `<section class="member-group"><h3>${group.title}</h3><div class="member-grid">${cards}</div></section>`;
    })
    .join("");
}

function renderOutputs(type = "papers") {
  currentOutputType = type;
  const list = document.getElementById("outputsList");
  list.innerHTML = (outputData[type] || [])
    .map((item) => {
      const d = item[currentLang];
      return `<article class="output-item"><p class="meta">${item.date}</p><h4>${d.title}</h4><p>${d.desc}</p></article>`;
    })
    .join("");
}

function renderNews() {
  const list = document.getElementById("newsList");
  list.innerHTML = newsData
    .map((item) => {
      const d = item[currentLang];
      return `<article class="news-item"><p class="meta">${item.date}</p><h4>${d.title}</h4><p>${d.desc}</p></article>`;
    })
    .join("");
}

function syncLanguageButtons() {
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang-btn") === currentLang);
  });
}

function applyLanguage() {
  applyStaticText();
  renderMembers();
  renderOutputs(currentOutputType);
  renderNews();
  syncLanguageButtons();
}

function setupTabs() {
  const tabs = document.querySelectorAll(".tab");
  tabs.forEach((tab) => {
    tab.addEventListener("click", () => {
      tabs.forEach((tbtn) => tbtn.classList.remove("active"));
      tab.classList.add("active");
      renderOutputs(tab.dataset.type);
    });
  });
}

function setupLanguageSwitcher() {
  document.querySelectorAll("[data-lang-btn]").forEach((btn) => {
    btn.addEventListener("click", () => {
      const lang = btn.getAttribute("data-lang-btn");
      if (!lang || lang === currentLang) return;
      currentLang = lang;
      localStorage.setItem("wm-lab-lang", currentLang);
      applyLanguage();
    });
  });
}

function setupMobileMenu() {
  const menuToggle = document.getElementById("menuToggle");
  const navLinks = document.getElementById("navLinks");
  menuToggle.addEventListener("click", () => navLinks.classList.toggle("show"));
  navLinks.querySelectorAll("a").forEach((a) => a.addEventListener("click", () => navLinks.classList.remove("show")));
}

function setupReveal() {
  const observer = new IntersectionObserver(
    (entries) => entries.forEach((e) => e.isIntersecting && e.target.classList.add("visible")),
    { threshold: 0.12 }
  );
  document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));
}

function setupYear() {
  document.getElementById("year").textContent = `© ${new Date().getFullYear()}`;
}

setupTabs();
setupLanguageSwitcher();
setupMobileMenu();
setupReveal();
setupYear();
applyLanguage();
